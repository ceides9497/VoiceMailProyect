package ucb.voicemail.presentation.tactil.viewmodel;

public class ChangePasscodeViewModel {

    private String title;
    private String buttonAcceptName;
    private String buttonCancelName;
    
    public String getTitle() {
        return title;
    }
    
    public void setTitle(String title) {
        this.title = title;
    }
    
    public String getButtonAcceptName() {
        return buttonAcceptName;
    }
    
    public void setButtonAcceptName(String buttonAcceptName) {
        this.buttonAcceptName = buttonAcceptName;
    }
    
    public String getButtonCancelName() {
        return buttonCancelName;
    }
    
    public void setButtonCancelName(String buttonCancelName) {
        this.buttonCancelName = buttonCancelName;
    }

}
